
class Vocabulary{
  String german;
  String italian;
  String additional;
  String id;

  Vocabulary(this.german, this.italian, this.additional, this.id);

  String getGerman(){
    return this.german;
  }

  String getItaliann(){
    return this.italian;
  }

  String getAdditional(){
    return this.additional;
  }

}